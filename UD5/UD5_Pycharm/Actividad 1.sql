(
    BASE NUMBER,
    ALTURA NUMBER,
    AREA NUMBER,
    CONSTRUCTOR FUNCTION RECTANGULO (BASE NUMBER, ALTURA NUMBER)
        RETURN SELF AS RESULT.
 );
 /
CREATE OR REPLACE TYPE BODY RECTANGULO AS
    CONSTRUCTOR FUNCTION RECTANGULO (BASE NUMBER, ALTURA NUMBER)
    RETURN SELF AS RESULT
    AS
    BEGIN
        SELF.BASE := BASE;
        SELF.ALTURA := ALTURA;
        SELF.AREA := BASE * ALTURA;
        RETURN;
    END;
END;
